CREATE TABLE `specializations` (
 `AFFILIATEID` int(2) NOT NULL,
 `UNITID` int(2) NOT NULL,
 `SPECIALIZATIONID` int(11) NOT NULL AUTO_INCREMENT,
 `FORM` int(2) NOT NULL,
 `DURATION` float NOT NULL,
 `ISACCREDITED` tinyint(1) NOT NULL,
 `STARTACCREDITATION` datetime DEFAULT NULL,
 `ENDACCREDITATION` datetime DEFAULT NULL,
 `DEGREEID` int(6) NOT NULL,
 `ISC:LOSED` tinyint(1) NOT NULL DEFAULT '0',
 `IUD` varchar(1) NOT NULL DEFAULT '1',
 PRIMARY KEY (`SPECIALIZATIONID`),
 KEY `affiliate_constr2` (`AFFILIATEID`),
 KEY `unit_constr` (`UNITID`),
 KEY `form_constr` (`FORM`),
 KEY `degree_constr` (`DEGREEID`),
 CONSTRAINT `affiliatecon` FOREIGN KEY (`AFFILIATEID`) REFERENCES `affiliate` (`ID`),
 CONSTRAINT `affiliateid_constr` FOREIGN KEY (`AFFILIATEID`) REFERENCES `affiliate` (`ID`),
 CONSTRAINT `degreecon` FOREIGN KEY (`DEGREEID`) REFERENCES `codensi` (`ID`),
 CONSTRAINT `degreeid_constr` FOREIGN KEY (`DEGREEID`) REFERENCES `codensi` (`ID`),
 CONSTRAINT `formcon` FOREIGN KEY (`FORM`) REFERENCES `edform` (`ID`),
 CONSTRAINT `formid_constr` FOREIGN KEY (`FORM`) REFERENCES `edform` (`ID`),
 CONSTRAINT `unitcon` FOREIGN KEY (`UNITID`) REFERENCES `basicunit` (`UNITID`)
) ENGINE=InnoDB DEFAULT CHARSET=latin1